  if( missing(x) ) stop('must specify x')
  if( !class(x) == 'H2OFrame' ) stop('x must be an h2o data object')
  res1 <- .h2o.unary_frame_op('year', x)
  .h2o.binary_frame_op("-", res1, 1900)
}

